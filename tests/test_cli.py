# ── dsm import ────────────────────────────────────────────────────────

class TestImport:
    def test_import_creates_db(self, runner, work_dir):
        xlsx = work_dir / "regmap_sample.xlsx"
        db = work_dir / "regmap_sample.db"

        result = runner.invoke(main, ["import", str(xlsx), "--db", str(db)])

        assert result.exit_code == 0, result.output
        assert db.exists()
        assert "Imported" in result.output
        assert "level2_common" in result.output

    def test_import_default_db_path(self, runner, work_dir):
        xlsx = work_dir / "regmap_sample.xlsx"

        result = runner.invoke(main, ["import", str(xlsx)])

        assert result.exit_code == 0, result.output
        # Default DB: same dir, same stem
        assert (work_dir / "regmap_sample.db").exists()


