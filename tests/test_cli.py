        xlsx = work_dir / "regmap_sample.xlsx"
        db_a = work_dir / "a.db"
        db_b = work_dir / "b.db"

        runner.invoke(main, ["import", str(xlsx), "--db", str(db_a)])

        # Copy and modify db_b
        shutil.copy(db_a, db_b)
        from dsm.models import init_db
        from dsm.domain_models import Register

        Session = init_db(f"sqlite:///{db_b}")
        with Session() as session:
            reg = session.query(Register).first()
            if reg:
                reg.init = "0xFF"
                session.commit()

        return db_a, db_b

