# ── dsm diff ──────────────────────────────────────────────────────────

class TestDiff:
    def _make_two_dbs(self, runner, work_dir) -> tuple[Path, Path]:
        """Create two DBs: original and a modified copy."""
        xlsx = work_dir / "regmap_sample.xlsx"
        db_a = work_dir / "a.db"
        db_b = work_dir / "b.db"

        runner.invoke(main, ["import", str(xlsx), "--db", str(db_a)])

        # Copy and modify db_b
        shutil.copy(db_a, db_b)
        from dsm.models import init_db
        from dsm.domain_models import Register

        Session = init_db(f"sqlite:///{db_b}")
        with Session() as session:
            reg = session.query(Register).first()
            if reg:
                reg.init = "0xFF"
                session.commit()

        return db_a, db_b

    def test_diff_two_dbs(self, runner, work_dir):
        db_a, db_b = self._make_two_dbs(runner, work_dir)

        result = runner.invoke(main, [
            "diff", str(db_a), str(db_b),
            "--db", str(work_dir / "diff.db"),
        ])

        assert result.exit_code == 0, result.output
        assert (work_dir / "diff.db").exists()
        assert "Saved to" in result.output

    def test_diff_identical(self, runner, work_dir):
        xlsx = work_dir / "regmap_sample.xlsx"
        db = work_dir / "same.db"
        runner.invoke(main, ["import", str(xlsx), "--db", str(db)])

        result = runner.invoke(main, [
            "diff", str(db), str(db),
            "--db", str(work_dir / "diff_same.db"),
        ])

        assert result.exit_code == 0, result.output


