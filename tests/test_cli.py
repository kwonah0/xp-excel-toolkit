# ── Formula roundtrip ────────────────────────────────────────────────

class TestFormulaRoundtrip:
    """Test that ArrayFormula and DataTableFormula survive import → export."""

    def test_array_formula_stored_correctly(self, tmp_path):
        """ArrayFormula.text and ref are stored in ExcelCell columns."""
        import openpyxl
        from openpyxl.worksheet.formula import ArrayFormula

        # Create xlsx with array formula
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "formulas"
        ws["A1"] = 10
        ws["A2"] = 20
        ws["B1"] = ArrayFormula(ref="B1:B2", text="=A1:A2*2")
        xlsx_path = tmp_path / "formulas.xlsx"
        wb.save(xlsx_path)
        wb.close()

        # Import
        from dsm.models import ExcelCell, init_db
        from dsm.xlsx_parser import import_xlsx

        db_path = tmp_path / "formulas.db"
        Session = init_db(f"sqlite:///{db_path}")
        with Session() as session:
            import_xlsx(session, xlsx_path, with_formulas=True)
            session.commit()

            # Verify formula_type and formula_ref stored
            cell = (
                session.query(ExcelCell)
                .filter(ExcelCell.formula_type == "array")
                .first()
            )
            assert cell is not None, "ArrayFormula cell not found in DB"
            assert cell.formula_ref == "B1:B2"
            assert cell.raw_value == "=A1:A2*2"

    def test_array_formula_export_roundtrip(self, tmp_path):
        """ArrayFormula survives import → DB → export_from_cells."""
        import openpyxl
        from openpyxl.worksheet.formula import ArrayFormula

        # Create xlsx with array formula
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "formulas"
        ws["A1"] = 10
        ws["B1"] = ArrayFormula(ref="B1:B2", text="=A1:A2*2")
        xlsx_path = tmp_path / "formulas.xlsx"
        wb.save(xlsx_path)
        wb.close()

        # Import
        from dsm.models import ExcelSheet, init_db
        from dsm.xlsx_parser import import_xlsx
        from dsm.exporter import export_from_cells

        db_path = tmp_path / "formulas.db"
        Session = init_db(f"sqlite:///{db_path}")
        with Session() as session:
            import_xlsx(session, xlsx_path, with_formulas=True)
            session.commit()

            sheet = session.query(ExcelSheet).first()
            out_path = tmp_path / "exported.xlsx"
            export_from_cells(session, sheet.id, out_path)

        # Verify exported xlsx has ArrayFormula
        wb2 = openpyxl.load_workbook(out_path, data_only=False)
        ws2 = wb2.active
        val = ws2["B1"].value
        assert isinstance(val, ArrayFormula), f"Expected ArrayFormula, got {type(val)}"
        assert val.text == "=A1:A2*2"
        assert val.ref == "B1:B2"
        wb2.close()

    def test_diff_moved_row_detection(self, tmp_path):
        """Smart diff detects rows that moved position without content change."""
        import openpyxl

        # Create old xlsx: rows A, B, C
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "test"
        ws["A1"], ws["B1"] = "Name", "Value"
        ws["A2"], ws["B2"] = "REG_A", "10"
        ws["A3"], ws["B3"] = "REG_B", "20"
        ws["A4"], ws["B4"] = "REG_C", "30"
        old_path = tmp_path / "old.xlsx"
        wb.save(old_path)
        wb.close()

        # Create new xlsx: rows A, C, B (B and C swapped)
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "test"
        ws["A1"], ws["B1"] = "Name", "Value"
        ws["A2"], ws["B2"] = "REG_A", "10"
        ws["A3"], ws["B3"] = "REG_C", "30"
        ws["A4"], ws["B4"] = "REG_B", "20"
        new_path = tmp_path / "new.xlsx"
        wb.save(new_path)
        wb.close()

        # Import both
        from dsm.models import init_db
        from dsm.xlsx_parser import import_xlsx

        db_old = tmp_path / "old.db"
        db_new = tmp_path / "new.db"

        S1 = init_db(f"sqlite:///{db_old}")
        with S1() as s:
            import_xlsx(s, old_path)
            s.commit()

        S2 = init_db(f"sqlite:///{db_new}")
        with S2() as s:
            import_xlsx(s, new_path)
            s.commit()

        # Run smart diff
        from dsm.diff.engine import diff_databases
        result = diff_databases(db_old, db_new, smart=True)

        moved = [c for c in result.cells if c.status == "moved"]
        assert len(moved) > 0, "Should detect moved rows"

        # Verify moved cells have both old_row and new_row
        for c in moved:
            assert c.old_row is not None
            assert c.new_row is not None
            assert c.old_row != c.new_row

        # Should not have any added/removed for the swapped rows
        added = [c for c in result.cells if c.status == "added"]
        removed = [c for c in result.cells if c.status == "removed"]
        assert len(added) == 0, f"Unexpected added cells: {added}"
        assert len(removed) == 0, f"Unexpected removed cells: {removed}"

